class FruitModel {
  final String id;
  final String name;
  final String description;
  final String color;
  final double price;
  final String imageUrl;
  final double sugarPercentage;

  FruitModel({
    required this.id,
    required this.name,
    required this.description,
    required this.color,
    required this.price,
    required this.imageUrl,
    required this.sugarPercentage,
  });
}
